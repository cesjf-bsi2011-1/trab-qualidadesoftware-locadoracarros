    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        String nome = tfNome.getText();
        String cpf = tfCPF.getText();
        String login = tfLogin.getText();
        String senha = tfSenha.getText();
        String dataNasc = tfDataNascimento.getText();

        if (nome.equals("") || cpf.equals("")) {
            JOptionPane.showMessageDialog(null, "Campos Obrigatorios não Preenchidos!");
        } else {
            func = new Funcionario();
            func.setNomeCompleto(nome);
            func.setCpf(cpf);
            func.setSexo("M");
            func.setUsuario(login);
            func.setSenha(senha);
            func.setSexo("M");
            DateFormat dtFormat = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date dataNascimento = dtFormat.parse(dataNasc);
                func.setDataNascimento(dataNascimento.getTime());
                Date dtCad = new Date();
                func.setDataCadastro(dtCad.getTime());
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Data Invalida!");
            }
            try {
                entity.persist(func);
                entity.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Funcionário Cadastrado com Sucesso!");
            } catch (Exception e) {
                entity.getTransaction().rollback();
                JOptionPane.showMessageDialog(null, "Algo Deu Errado, Funcionário não Cadastrado!");
            }
        }
        atualizaTabela();
        limpaCampos();
        btEditar.setEnabled(false);
        btSalvar.setEnabled(false);
        btExcluir.setEnabled(false);
        btNovo.setEnabled(true);
    }//GEN-LAST:event_btSalvarActionPerformed

