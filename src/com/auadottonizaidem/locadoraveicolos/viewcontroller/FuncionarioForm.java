    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        // TODO add your handling code here:

        String nome = tfNome.getText();
        String cpf = tfCPF.getText();
        String dataNasc = tfDataNascimento.getText();
        String login = tfLogin.getText();

        Funcionario f = entity.find(Funcionario.class, id);

        if (nome.equals("") || cpf.equals("") || login.equals("")) {
            JOptionPane.showMessageDialog(null, "Campos Obrigatorios não Preenchidos!");
        } else {
            f.setNomeCompleto(nome);
            f.setCpf(cpf);
            f.setSexo("M");
            f.setUsuario(login);
            DateFormat dtFormat = new SimpleDateFormat("dd/MM/yyyy");
            try {
                Date dataNascimento = dtFormat.parse(dataNasc);
                f.setDataNascimento(dataNascimento.getTime());
                Date dtCad = new Date();
                f.setDataCadastro(dtCad.getTime());
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Data Invalida!");
            }
            try {
                entity.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Funcionário Editado com Sucesso!");
            } catch (Exception e) {
                entity.getTransaction().rollback();
                JOptionPane.showMessageDialog(null, "Algo Deu Errado, Funcionário não Editado!");
            }
        }
        atualizaTabela();
        limpaCampos();
    }//GEN-LAST:event_btEditarActionPerformed

